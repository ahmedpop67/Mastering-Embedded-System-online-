#include "STD_TYPES.h"
#include "BIT_MATH.h"
#include  "GI_interface.h"

#define SREG   *((volatile u8*)0x5f)

void GI_viodEnable(void){
	SET_BIT(SREG,7);
}

void GI_viodDisable(void){
	CLR_BIT(SREG,7);
}